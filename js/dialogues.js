// 根据tile类型和状态，获取对应的台词key
export function getDialogueKey(tile) {
  const type = tile.type;
  const civLevel = gameState.civilizationLevel || 1;

  if (type === 'settlement') {
    return 'settlement_lv' + Math.min(civLevel, 3);
  }
  if (type === 'quarry' && (tile.quarryLevel || 1) >= 2) {
    return 'quarry_upgraded';
  }
  if (type === 'lumber_mill' && (tile.lumberLevel || 1) >= 2) {
    return 'lumber_mill_upgraded';
  }
  return type;
}

// 随机选取一句台词（避免连续重复）
const lastDialogueIndex = {};
export function pickDialogue(key) {
  const lines = BUILDING_DIALOGUES[key];
  if (!lines || lines.length === 0) return null;
  let idx;
  if (lines.length === 1) {
    idx = 0;
  } else {
    const last = lastDialogueIndex[key];
    do {
      idx = Math.floor(Math.random() * lines.length);
    } while (idx === last);
  }
  lastDialogueIndex[key] = idx;
  return lines[idx];
}

